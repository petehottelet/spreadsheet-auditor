"""Benchmark the auditor against the seeded-defect catalog.

Runs `spreadsheet-auditor` against every workbook listed in `seeded_defects.json`,
diffs the produced findings against the expected defects, and emits a
Markdown matrix to `benchmarks/seeded_defects_matrix.md`.

Honesty contract:
- DET findings missed in a recalc-capable environment are counted as Missed.
- Value-dependent checks (`requires_recalc: true`) only count as Missed if
  recalculation is actually available; otherwise they're flagged "n/a (no recalc)".
- Findings the auditor reports that have no matching seed are counted as
  unexpected (potential false positives). They are listed by rule and location
  so reviewers can investigate.

Exit code:
- 0 when no DET defect is missed in a recalc-capable environment AND no new
  unexpected findings appeared compared to a previous matrix (no regression).
- 0 otherwise too -- the benchmark is informational; CI surfaces the matrix as
  an artifact rather than a hard gate. Set `--strict` to fail on regressions.
"""

from __future__ import annotations

import argparse
import json
import subprocess
import sys
from collections import defaultdict
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
DEFAULT_CATALOG = ROOT / "benchmarks" / "seeded_defects.json"
DEFAULT_MATRIX = ROOT / "benchmarks" / "seeded_defects_matrix.md"


def _normalize(location: str) -> str:
    return location.replace(" ", "")


def run_audit(workbook: Path) -> tuple[dict, bool]:
    """Run the auditor and return (payload, recalc_available)."""
    cmd = [
        sys.executable,
        "-m",
        "spreadsheet_auditor",
        str(workbook),
        "--json",
        "-",
    ]
    result = subprocess.run(cmd, capture_output=True, text=True)
    if result.returncode not in (0, 1, 2):
        raise RuntimeError(
            f"Auditor exited {result.returncode} for {workbook}:\n{result.stderr}"
        )
    try:
        payload = json.loads(result.stdout)
    except json.JSONDecodeError as exc:
        raise RuntimeError(f"Auditor did not emit valid JSON for {workbook}: {exc}")
    recalc_status = payload.get("workbook", {}).get("recalc_status", "unknown")
    return payload, recalc_status == "completed"


def diff_workbook(seeded: list[dict], payload: dict, recalc_available: bool) -> dict:
    detected = defaultdict(set)
    for finding in payload.get("findings", []):
        if finding.get("suppressed"):
            continue
        detected[finding["rule_id"]].add(_normalize(finding["location"]))

    rows: list[dict] = []
    seeded_lookup: set[tuple[str, str]] = set()
    for entry in seeded:
        rule = entry["rule_id"]
        loc = _normalize(entry["location"])
        seeded_lookup.add((rule, loc))
        was_detected = loc in detected.get(rule, set())
        if entry["requires_recalc"] and not recalc_available:
            status = "n/a (no recalc)"
        elif was_detected:
            status = "detected"
        else:
            status = "MISSED"
        rows.append(
            {
                "rule_id": rule,
                "location": entry["location"],
                "mode": entry["mode"],
                "requires_recalc": entry["requires_recalc"],
                "status": status,
                "notes": entry.get("notes", ""),
            }
        )

    # Surface unexpected findings (potential false positives) by rule.
    unexpected: list[tuple[str, str]] = []
    for rule, locations in detected.items():
        for loc in locations:
            if (rule, loc) not in seeded_lookup:
                unexpected.append((rule, loc))
    unexpected.sort()
    return {"rows": rows, "unexpected": unexpected, "recalc_available": recalc_available}


def render_matrix(catalog: dict, results: list[dict]) -> str:
    lines: list[str] = []
    lines.append("# Seeded Defects Matrix")
    lines.append("")
    lines.append(
        "Automatically generated by `python benchmarks/run_benchmark.py`. Do not "
        "edit by hand. See [benchmark_methodology.md](../references/benchmark_methodology.md)."
    )
    lines.append("")

    # Summary
    total_seeded = 0
    total_detected = 0
    total_missed = 0
    total_na = 0
    total_unexpected = 0
    for r in results:
        for row in r["rows"]:
            total_seeded += 1
            if row["status"] == "detected":
                total_detected += 1
            elif row["status"] == "MISSED":
                total_missed += 1
            else:
                total_na += 1
        total_unexpected += len(r["unexpected"])
    detection_rate = (
        f"{(total_detected / max(total_seeded - total_na, 1)) * 100:.0f}%"
        if total_seeded
        else "n/a"
    )
    lines.append("## Summary")
    lines.append("")
    lines.append(f"- Seeded defects: {total_seeded}")
    lines.append(f"- Detected: {total_detected}")
    lines.append(f"- Missed: {total_missed}")
    lines.append(f"- Skipped (value-dependent, no recalc available): {total_na}")
    lines.append(f"- Unexpected findings (potential false positives): {total_unexpected}")
    lines.append(f"- Detection rate (excluding skipped): **{detection_rate}**")
    lines.append("")

    # Per-workbook tables
    for r, source in zip(results, catalog["workbooks"]):
        lines.append(f"## {source['path']}")
        lines.append("")
        recalc_state = "available" if r["recalc_available"] else "unavailable (cached values only)"
        lines.append(f"_Recalculation: {recalc_state}._")
        lines.append("")
        lines.append("| Rule | Location | Mode | Requires Recalc | Status | Notes |")
        lines.append("| --- | --- | --- | --- | --- | --- |")
        for row in r["rows"]:
            lines.append(
                "| "
                + " | ".join(
                    [
                        row["rule_id"],
                        row["location"],
                        row["mode"],
                        "yes" if row["requires_recalc"] else "no",
                        row["status"],
                        row["notes"],
                    ]
                )
                + " |"
            )
        lines.append("")
        if r["unexpected"]:
            lines.append("**Unexpected findings (no matching seed):**")
            lines.append("")
            for rule, loc in r["unexpected"]:
                lines.append(f"- `{rule}` at `{loc}`")
            lines.append("")
    return "\n".join(lines) + "\n"


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--catalog", default=str(DEFAULT_CATALOG))
    parser.add_argument("--out", default=str(DEFAULT_MATRIX))
    parser.add_argument(
        "--strict",
        action="store_true",
        help="Fail (exit 1) when any DET defect was missed (and recalc was not required) or any unexpected finding appeared.",
    )
    args = parser.parse_args(argv)

    catalog = json.loads(Path(args.catalog).read_text(encoding="utf-8"))
    results: list[dict] = []
    for entry in catalog["workbooks"]:
        wb_path = ROOT / entry["path"]
        if not wb_path.exists():
            print(f"warning: workbook not found, skipping: {wb_path}", file=sys.stderr)
            results.append({"rows": [], "unexpected": [], "recalc_available": False})
            continue
        payload, recalc_available = run_audit(wb_path)
        results.append(diff_workbook(entry["seeded"], payload, recalc_available))

    matrix = render_matrix(catalog, results)
    Path(args.out).write_text(matrix, encoding="utf-8")
    print(f"Wrote {args.out}")

    if args.strict:
        regressions = 0
        for r in results:
            regressions += sum(1 for row in r["rows"] if row["status"] == "MISSED")
            regressions += len(r["unexpected"])
        if regressions:
            print(f"Strict mode: {regressions} regression(s) detected.", file=sys.stderr)
            return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
